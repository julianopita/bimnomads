<p class="paragraph2">Divide-se o processo em <span class="azul">ETAPAS.</span><br />
  Cada etapa é referente a algum aspecto específico da obra, aqui chamado de <span class="azul">TÓPICO</span>,<br /> como por exemplo &quot;Volumetria&quot; ou &quot;Implantação&quot;.<br /> Uma mesma etapa pode ter diversos tópicos.</p>
<p class="paragraph2">Uma etapa, após aberta pela equipe técnica ou moderador, é organizada em <span class="azul">FASES</span>.</p>
<p class="paragraph2" ><span class="laranja">Na primeira fase</span> de uma etapa,<br /> é feito o lançamento do projeto e é dado um tempo para que você possa analisar as informações divulgadas.</p>
<p class="paragraph2"><span class="laranja">Na segunda fase</span>, chamada de <span class="azul">CONSULTA</span><br /> é possível então que você insira sugestões, comentários e discussões sobre os tópicos abertos nesta etapa do projeto lançado. Para isso, você pode começar uma <span class="azul">CONVERSA</span> dentro dos tópicos ou comentar em conversas já criadas.</p>
<p class="paragraph2"><span class="laranja">Na terceira fase</span>,<br /> a equipe técnica tem um tempo para avaliar as possibilidades levantadas e reponder às conversas.</p>
<p class="paragraph2"><span class="laranja">Na quarta fase</span>,<br /> chamada de <span class="azul">RÉPLICA</span>, você pode responder os comentários da equipe técnica e moderadores.</p>
<p class="paragraph2"><span class="laranja">Na quinta e última fase</span>,<br /> são estabelecidas as <span class="azul">CONCLUSÕES</span> do que será feito na obra a partir daquela conversa.</p>

<span class="sobreprojeto-right">
  <p>A exposição<strong> “Peles Contemporâneas”</strong> é parte de uma pesquisa que visa produzir conhecimento sobre sistemas de proteção solar para formas arquitetônicas com geometrias complexas. O objetivo do projeto é explorar inter-relações e colaborações possíveis entre processos de design paramétrico e técnicas de fabricação digital, na concepção, produção e funcionamento de sistemas interativos de vedação com geometrias complexas.</p> 
<p>Além de procedimentos clássicos de pesquisa, a metodologia inclui a concepção e experimentação de modelos digitais e físicos através do uso de tecnologias de fabricação digital instaladas no Instituto de Arquitetura e Urbanismo da Universidade de São Paulo, visando antecipar aspectos de design, produção e funcionamento dos sistemas propostos.</p>
<p>Financiado pela Pro-reitoria de Pesquisa da USP, o projeto desenvolve-se no Nomads.usp - Núcleo de Estudos de Habitares Interativos, do IAU-USP, onde esses temas vêm sendo tratados, desde 2006, em pesquisas financiadas por agências públicas.
</p>


  <p>O Projeto <strong>APAE Itapevi</strong> se trata da reestruturação dos fluxos e atividades realizados na área externa da edificação, além da definição de layout e circulação vertical para um novo pavimento com salas de aulas.</p>
  <p>A APAE realiza atividades pedagógicas com crianças, adolescentes e adultos com paralisia cerebral, síndrome de down e autismo. Dessa forma, o processo de concepção do projeto tem-se baseado no desenvolvimento motor e cognitivo dos educandos por meio da elaboração de espaços lúdicos.</p>
  <p>Tal trabalho se iniciou durante o mês de julho em que desde então foram realizadas visitas para dialogar com a direção e os servidores para compreender as demandas e discussões com relação ao espaço, bem como para realizar o levantamento métrico do local.</p>
  <p>Entre os desafios identificados nesse processo está o estabelecimento de um contato de fato participativo entre a equipe técnica e os usuários do espaço. Nesse contexto, a equipe conta com a esta plataforma para aplicar novos métodos e ferramentas a fim de estreitar este diálogo durante as próximas etapas do projeto.</p>



